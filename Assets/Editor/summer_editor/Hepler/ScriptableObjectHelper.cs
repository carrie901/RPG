using UnityEngine;

namespace SummerEditor
{
    public class ScriptableObjectHelper
    {

        public static T Create<T>() where T : ScriptableObject
        {
            T t = ScriptableObject.CreateInstance<T>();
            return t;
        }

        public static void Save<T>(T t, string path_dir, string asset_name) where T : ScriptableObject
        {
            UnityEditor.AssetDatabase.CreateAsset(t, path_dir + "/" + asset_name + ".asset");
        }
    }
}