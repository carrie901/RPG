using UnityEngine;

namespace Summer
{

    public class ResInfo : ObjectInfo
    {
        public ResInfo(Object obj, string resPath) : base(obj, resPath) { }
        public override void UnLoad()
        {
            UnityEngine.Resources.UnloadAsset(_object);
            _object = null;
        }
    }
}